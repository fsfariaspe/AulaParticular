package AulaParticular;

/*12.	Faça um algoritmo em Java que imprima todos os números de 150 a 300.*/
public class Exe12 {
    public static void main(String[] args) {

        int num = 150;

        while(num <= 300){
            System.out.println(num);
            num++;
        }
        System.out.println("Fim!");
    }
}
